import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const rk = path.join(
  __dirname,
  "../node_modules/@rainbow-me/rainbowkit/dist/wallets/walletConnectors",
)

function readDataUrl(filename) {
  const s = fs.readFileSync(path.join(rk, filename), "utf8")
  const m = s.match(/var \w+ = "(data:image[^"]+)"/)
  if (!m) {
    throw new Error(`no data url in ${filename}`)
  }
  return m[1]
}

const metaMask = readDataUrl("metaMaskWallet-SITXT2FV.js")
const coinbase = readDataUrl("coinbaseWallet-OKXU3TRC.js")
const rabby = readDataUrl("rabbyWallet-GFVPHCTK.js")
const taho = readDataUrl("tahoWallet-K4GKE2XM.js")
const walletConnect = readDataUrl("walletConnectWallet-YHWKVTDY.js")

const out = `// Generated by scripts/gen-wallet-icons.mjs from RainbowKit bundled SVGs — run after upgrading @rainbow-me/rainbowkit
export const WALLET_ICON_DATA_URLS = {
  metaMask: ${JSON.stringify(metaMask)},
  coinbase: ${JSON.stringify(coinbase)},
  rabby: ${JSON.stringify(rabby)},
  taho: ${JSON.stringify(taho)},
  walletConnect: ${JSON.stringify(walletConnect)},
} as const
`

const dest = path.join(
  __dirname,
  "../src/components/ConnectWalletDrawer/walletIconUrls.ts",
)
fs.writeFileSync(dest, out)
console.log("wrote", dest, "bytes", out.length)
